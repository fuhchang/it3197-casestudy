package com.example.it3197_casestudy.model;

public class User {
	private String userNRIC;
	private String name;
	private int age;
	private String email;
	private String password;
	private String phoneNumber;
	private String homeLocation;
	private String gender;
	/**
	 * @return the userNRIC
	 */
	public String getUserNRIC() {
		return userNRIC;
	}
	/**
	 * @param userNRIC the userNRIC to set
	 */
	public void setUserNRIC(String userNRIC) {
		this.userNRIC = userNRIC;
	}
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	/**
	 * @return the age
	 */
	public int getAge() {
		return age;
	}
	/**
	 * @param age the age to set
	 */
	public void setAge(int age) {
		this.age = age;
	}
	/**
	 * @return the email
	 */
	public String getEmail() {
		return email;
	}
	/**
	 * @param email the email to set
	 */
	public void setEmail(String email) {
		this.email = email;
	}
	/**
	 * @return the password
	 */
	public String getPassword() {
		return password;
	}
	/**
	 * @param password the password to set
	 */
	public void setPassword(String password) {
		this.password = password;
	}
	/**
	 * @return the phoneNumber
	 */
	public String getPhoneNumber() {
		return phoneNumber;
	}
	/**
	 * @param phoneNumber the phoneNumber to set
	 */
	public void setPhoneNumber(String phoneNumber) {
		this.phoneNumber = phoneNumber;
	}
	/**
	 * @return the homeLocation
	 */
	public String getHomeLocation() {
		return homeLocation;
	}
	/**
	 * @param homeLocation the homeLocation to set
	 */
	public void setHomeLocation(String homeLocation) {
		this.homeLocation = homeLocation;
	}
	/**
	 * @return the gender
	 */
	public String getGender() {
		return gender;
	}
	/**
	 * @param gender the gender to set
	 */
	public void setGender(String gender) {
		this.gender = gender;
	}
}
